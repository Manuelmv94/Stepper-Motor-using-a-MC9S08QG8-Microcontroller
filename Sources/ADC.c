void ADC_Init(void)
{
   ADC_EnablePinForADCv1(ADC_CHANNEL);
   ADC_ConversionModeSelection(ADC_RESOLUTION);
   ADC_SelectChannel(ADC_CHANNEL);
   ADC_EnableContinuousConversion();
}

