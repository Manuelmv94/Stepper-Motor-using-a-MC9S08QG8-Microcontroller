/*!*-----------------------------------------------------------------------------------------------
* \name     ModeManager_Init
* \brief    It initializes the Manager to rotate on Clockwise mode
* \param    void
* \return   void
-------------------------------------------------------------------------------------------------*/
void ModeManager_Init(void);

/*!*-----------------------------------------------------------------------------------------------
* \name     ModeManager_IsClockWise
* \brief    It returns TRUE if the manager is set to Clockwise mode, and FALSE on Counter-Clockwise mode
* \param    bool
* \return   void
-------------------------------------------------------------------------------------------------*/
bool ModeManager_IsClockWise(void);

/*!*-----------------------------------------------------------------------------------------------
* \name     ModeManager_SetDirection
* \brief    It sets the manager to rotate in either Clockwise or Counter-Clockwise mode.
*           TRUE(bool)  sets the manager to Clockwise mode
*           FALSE(bool) sets the manager to Counter-Clockwise mode
* \param    bool
* \return   void
-------------------------------------------------------------------------------------------------*/
void ModeManager_SetDirection(bool);


