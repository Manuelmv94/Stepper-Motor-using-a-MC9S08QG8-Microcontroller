/*!*-----------------------------------------------------------------------------------------------
* \name     PowerManager_Init
* \brief    It initializes on OFF mode (motor stopped)
* \param    void
* \return   void
-------------------------------------------------------------------------------------------------*/
void PowerManager_Init(void);

/*!*-----------------------------------------------------------------------------------------------
* \name     PowerManager_IsMotorON
* \brief    It returns TRUE if the manager is set to ON mode, and FALSE on OFF mode
* \param    bool
* \return   void
-------------------------------------------------------------------------------------------------*/
bool PowerManager_IsMotorON(void);

/*!*-----------------------------------------------------------------------------------------------
* \name     PowerManager_setMotor
* \brief    It sets the manager to in either ON(rotating) or OFF(stopped) mode.
*           TRUE(bool)  sets the manager to ON
*           FALSE(bool) sets the manager to OFF
* \param    bool
* \return   void
-------------------------------------------------------------------------------------------------*/
void PowerManager_setMotor(bool);

